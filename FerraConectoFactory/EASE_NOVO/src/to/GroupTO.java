package to;

public class GroupTO {

    private String codPerfil;
    private String desPerfil;
    private String tipPerfil;
    
    /**
     * @return the codPerfil
     */
    public String getCodPerfil() {
        return codPerfil;
    }
    /**
     * @param codPerfil the codPerfil to set
     */
    public void setCodPerfil(String codPerfil) {
        this.codPerfil = codPerfil;
    }
    /**
     * @return the desPerfil
     */
    public String getDesPerfil() {
        return desPerfil;
    }
    /**
     * @param desPerfil the desPerfil to set
     */
    public void setDesPerfil(String desPerfil) {
        this.desPerfil = desPerfil;
    }
    /**
     * @return the tipPerfil
     */
    public String getTipPerfil() {
        return tipPerfil;
    }
    /**
     * @param tipPerfil the tipPerfil to set
     */
    public void setTipPerfil(String tipPerfil) {
        this.tipPerfil = tipPerfil;
    }
    
}
